  class StationBeanByNameSorter implements Comparator<StationBean> {

    @Override
    public int compare(StationBean a, StationBean b) {
      //Avoid null pointers
      String aa = a.getName();
      if (aa == null) {
        aa = "aaa";
      }
      String bb = b.getName();
      if (bb == null) {
        bb = "aaa";
      }

      return aa.compareTo(bb);
    }
  }

  class StationBeanListModel extends AbstractListModel<StationBean> {

    private static final long serialVersionUID = 5484763343433191947L;

    private final List<StationBean> all;

    public StationBeanListModel() {
      all = new ArrayList<>();
    }

    @Override
    public int getSize() {
      return all.size();
    }

    @Override
    public StationBean getElementAt(int index) {
      return (StationBean) all.toArray()[index];
    }

    public void add(StationBean element) {
      if (all.add(element)) {
        Collections.sort(all, new StationBeanByNameSorter());
        fireContentsChanged(this, 0, getSize());
      }
    }

    public void addAll(StationBean elements[]) {
      Collection<StationBean> c = Arrays.asList(elements);
      all.addAll(c);
      Collections.sort(all, new StationBeanByNameSorter());
      fireContentsChanged(this, 0, getSize());
    }

    public void addAll(Collection<StationBean> elements) {
      all.addAll(elements);
      Collections.sort(all, new StationBeanByNameSorter());
      fireContentsChanged(this, 0, getSize());
    }

    public void clear() {
      all.clear();
      fireContentsChanged(this, 0, getSize());
    }

    public boolean contains(StationBean element) {
      return all.contains(element);
    }

    public StationBean firstElement() {
      if (!all.isEmpty()) {
        return all.get(0);
      } else {
        return null;
      }
    }

    public Iterator<StationBean> iterator() {
      return all.iterator();
    }

    public StationBean lastElement() {
      if (!all.isEmpty()) {
        return all.get(all.size() - 1);
      } else {
        return null;
      }
    }

    public boolean removeElement(StationBean element) {
      boolean removed = all.remove(element);
      if (removed) {
        Collections.sort(all, new StationBeanByNameSorter());
        fireContentsChanged(this, 0, getSize());
      }
      return removed;
    }
  }

